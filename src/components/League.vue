                        <h2 class="text-center">Sides and Drafting Order</h2>

                        <h3>Selection Priority</h3>
                        <p>
                          The lobby owner sets Selection Priority to "Automatic (Coin Toss)".
                        </p>
                        <p>
                          If for some reason this ingame function does not work, Selection Priority must be set to "Manual". 
                          The following is a full explanation of the different game modes:
                        </p>
                        <ul>
                          <li>
                            One (1) game The lobby owner chooses "heads" or "tails" for their team and tosses a coin using the "/flip" command in the lobby chat once there is at least 1 player from each team in the lobby. The coin toss winner may then choose one of options: Radiant or Dire.
                          </li>
                          <li>
                            Two (2) game series At the start of the first game the lobby owner chooses "heads" or "tails" for their team and tosses a coin using the "/flip"
                            command in the lobby chat once there is at least 1 player from each team in the lobby. 
                            The coin toss winner (Team A) can then choose one of the options: Radiant, Dire. For the second game the sides are reversed.
                          </li>
                          <li>
                            Best of three (Bo3) At the start of the first game the lobby owner chooses "heads" or "tails" for their team and tosses a coin using the "/flip" command in 
                            the lobby chat once there is at least 1 player from each team in the lobby. The coin toss winner (Team A) can then choose one of the options: Radiant, Dire. 
                            For the second game the roles are reversed. The third game follows the same procedure as the first with a new coin toss.
                          </li>
                        </ul>

